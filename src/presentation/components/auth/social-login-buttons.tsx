const providerIcons: Record<string, ReactNode> = {
  google: <GoogleIcon />,
  facebook: <FacebookIcon />,
  linkedin_oidc: <LinkedInIcon />,
};

const providers: { name: string; provider: Provider }[] = [
  { name: "Google", provider: "google" },
  { name: "Facebook", provider: "facebook" },
];

export function SocialLoginButtons() {
  const supabase = createClient();

  const handleSocialLogin = async (provider: Provider) => {
    // Sign out anonymous session first â€” it blocks OAuth initiation
    const { data: { user } } = await supabase.auth.getUser();
    if (user?.is_anonymous) {
      await supabase.auth.signOut();
    }

    await supabase.auth.signInWithOAuth({
      provider,
      options: {
        redirectTo: `${getBaseUrl()}/callback`,
      },
    });
  };

  return (
    <div className="flex flex-col gap-2">
      {providers.map(({ name, provider }) => (
        <Button
          key={provider}
          variant="outline"
          className="w-full gap-2"
          onClick={() => handleSocialLogin(provider)}
        >
          {providerIcons[provider]}
          Continue with {name}
        </Button>
      ))}
    </div>
  );
}
